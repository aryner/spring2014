###
#	[2/-x2      ,  (1-2e)/(1-e)]
#			vs
#	[1/e(1-x2)  ,  (2-1/e)/(1-1/e)]
###

def s1(e):
	x2 = (1-2*e)/(1-e)
	x1 = 2/(-x2)
	print('s1(%f) = [ %.15f, %.15f ] ' %(e,x1,x2))
	return

def s2(e):
	x2 = (2-1/e)/(1-(1/e))
	x1 = 1/e*(1-x2)
	print('s2(%f) = [ %.15f, %.15f ] ' %(e,x1,x2))
	return

s1(0.9)
s1(0.1)
s1(0.01)
s1(0.001)
s1(0.0001)
s1(0.00001)
s1(0.000001)
